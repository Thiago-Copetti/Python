# Tipo de dado bool (booleano) representa valores de verdade,
#  ou seja, True (verdadeiro) e False (falso).

# Exemplos de uso do tipo bool:
# Variável do tipo bool
estudando = True
print(estudando)  # Output: True

# Variável do tipo bool
chovendo = False
print(chovendo)  # Output: False

# O tipo bool é frequentemente usado em estruturas de controle, como if, while, etc.