# Strings em Python

# Strings são sequências de caracteres, utilizadas para armazenar e manipular texto.
# As strings podem ser criadas usando aspas simples (' '), aspas duplas (" ")
# ou aspas triplas (''' ''' ou """ """).
# As strings são imutáveis, ou seja, uma vez criadas, não podem ser alteradas.
# No entanto, podemos criar novas strings a partir de operações como concatenação 
# e fatiamento. As string são iteráveis, ou seja, podemos percorrer cada caractere 
# da string usando um loop.

# Criando strings
string1 = 'Olá, mundo!'  # Usando aspas simples
string2 = "Olá, mundo!"  # Usando aspas duplas
string3 = '''Olá, mundo!'''  # Usando aspas triplas

