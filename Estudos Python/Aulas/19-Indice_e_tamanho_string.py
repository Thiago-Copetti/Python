# Índice e tamanho de strings

# Strings são iteráveis, ou seja, podemos acessar cada 
# caractere individualmente usando índices. O índice de 
# uma string começa em 0 para o primeiro caractere, 
# 1 para o segundo, e assim por diante.

# Exemplo de acesso a caracteres usando índices

texto = "Olá, mundo!"
print(texto[0])  # Imprime 'O'
print(texto[1])  # Imprime 'l'
print(texto[2])  # Imprime 'á'
print(texto[7])  # Imprime 'm'

# Tamanho de strings

# Podemos obter o tamanho de uma string usando a função len().

tamanho = len(texto)
print(tamanho)  # Imprime 13, pois "Olá, mundo!" tem 13 caracteres

