# End e Sep em Python 

# \r\n # \n é o caractere de nova linha
# sep é o argumento nomeado que define o separador entre os valores
# end é o argumento nomeado que define o que será impresso no final



print('Texto',True, 1234, sep="-", end='##\n') #sep # é o separador entre os valores
print(56,78, sep='-', end='\n##')
print(9,10, sep='-')