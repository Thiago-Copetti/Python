# Índices e Modificação de Listas em Python

# Criando uma lista de frutas
frutas = ["maçã", "banana", "laranja", "uva"]

# Acessando elementos da lista usando índices

print(frutas[0])  # Output: maçã
print(frutas[1])  # Output: banana
print(frutas[2])  # Output: laranja

# Modificando elementos da lista usando índices

frutas[1] = "abacaxi"  # Modificando "banana" para "abacaxi"
print(frutas)  # Output: ['maçã', 'abacaxi', 'laranja', 'uva']

