# Descobrindo o Índice de um Elemento em uma Lista em Python

# Criando uma lista de frutas
frutas = ["maçã", "banana", "laranja", "uva"]

# Descobrindo o índice de um elemento usando o método index()

# indice = lista.index('item')

indice_banana = frutas.index("banana")
print(f"O índice de 'banana' é: {indice_banana}")  # Output: O índice de 'banana' é: 1
